
public class BitwiseOperatorJava {
    public static void main(String[] args) {

        int output = 5 ^ 2;
        System.out.println("The result of 5 ^ 2 is: " + output);

        output = 5  | 2;
        System.out.println("The result of 5 | 2 is: " + output);

        output = 5  & 2;
        System.out.println("The result of 5 & 2 is: " + output);

        output = 5  >> 2;
        System.out.println("The result of 5 >> 2 is: " + output);

        output = 5  << 2;
        System.out.println("The result of 5 << 2 is: " + output);

    }
}


/*
 * bitwise operator
 * 
 * 1) & -- operator
 * 2) | -- operator
 * 3) ^ -- operator
 * 4) << -- left shift
 * 5) >> -- right shift
 */
